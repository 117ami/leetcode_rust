impl Solution {
    pub fn sort_array_by_parity_ii(a: Vec<i32>) -> Vec<i32> {
        let mut res = vec![0; a.len()];
        let mut i = 0; 
        let mut j = 1; 
        for n in a{
            if n % 2 == 0 {
                res[i] = n; 
                i += 2; 
            } else {
                res[j] = n; 
                j += 2;
            }
        }
        res 
    }
}


